package com.rdalabs.automation.testing.exp;

public class ReportingAutomationException extends AutomationException {

  private static final long serialVersionUID = -4890517758142173721L;

  public ReportingAutomationException() {
    super();
  }

  public ReportingAutomationException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

  public ReportingAutomationException(String message, Throwable cause) {
    super(message, cause);
  }

  public ReportingAutomationException(String message) {
    super(message);
  }

  public ReportingAutomationException(Throwable cause) {
    super(cause);
  }

}
